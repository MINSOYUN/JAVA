package methodEx;

public class calcul {
		/** 
		 * 정사각형의 넓이 구하기
		 * width
		 * return width * width
		 */
	public double  areaRectangle(double width) { // 메서드(double 타입)
		return width * width;
	}
	
		/** 
		 * 직사각형의 넓이 구하기
		 * width
		 * height
		 * return width * height
		 */
	public double areaRectangle(double width, double height) {// 메서드(double, double 타입)
		return width * height;
	} // areaRectangle 끝
	}
